CREATE TABLE T_FEATURECLASS(
  ID  NUMERIC(8,0) GENERATED BY DEFAULT AS IDENTITY (START WITH 1000) PRIMARY KEY,
  CLASSCODE VARCHAR(5) NOT NULL,
  SHORTDESC VARCHAR(50) NOT NULL,
  DESCRIPTION  VARCHAR(255)
);

CREATE TABLE T_CENSUSCODE(
  ID  NUMERIC(8,0) GENERATED BY DEFAULT AS IDENTITY (START WITH 1000) PRIMARY KEY,
  STATECODE NUMERIC(2,0) NOT NULL,
  COUNTYCODE  NUMERIC(3,0) NOT NULL,
  STATENAME VARCHAR(25) NOT NULL,
  COUNTYNAME  VARCHAR(25) NOT NULL
);

CREATE TABLE T_BORDERPOINT(
  ID  NUMERIC(12,0) GENERATED BY DEFAULT AS IDENTITY (START WITH 1000) PRIMARY KEY,
  STATECODEID NUMERIC(2,0) NOT NULL,
  COUNTYCODEID  NUMERIC(3,0) NOT NULL,
  LATITUDE  NUMERIC(9,5) NOT NULL,
  LONGITUDE NUMERIC(9,5) NOT NULL,
  FEATURECLASSID  NUMERIC(8,0),
  CONSTRAINT FK_FEATURECLASS FOREIGN KEY(FEATURECLASSID) REFERENCES T_FEATURECLASS(ID)
);

CREATE TABLE T_STATE(
  ID  NUMERIC(8,0) GENERATED BY DEFAULT AS IDENTITY (START WITH 1000) PRIMARY KEY,
    STATENAME VARCHAR(25),
    STATEABBR VARCHAR(2),
    STATEFIPS VARCHAR(5),
    CONSTRAINT UNIQUE(STATENAME),
    CONTRAINT UNIQUE(STATEABBR)
);

CREATE TABLE T_LOCATION(
  ID  NUMERIC(8,0) GENERATED BY DEFAULT AS IDENTITY (START WITH 1000) PRIMARY KEY,
  STATEID NUMERIC(8,0),
  LOCATIONNAME  VARCHAR(25),
  LOCATIONFIPS  VARCHAR(10),
  FEATURECLASSID NUMERIC(8,0),
  CONSTRAINT fk_loc_state FOREIGN KEY(STATEID) REFERENCES T_STATE(ID),
  CONSTRAINT fk_loc_featureclass FOREIGN KEY(FEATURECLASSID) REFERENCES T_FEATURECLASS(ID)
);